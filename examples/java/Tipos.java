package main;

public class Tipos {

    public static void main(final String[] array) {
		System.out.println("Is a String: hello world");
		System.out.println("Is a Int: " + 0);
		System.out.println("Is a Long: " + 0L);
		System.out.println("Is a Double: " + 0D);
		System.out.println("Is a Float: " + 0F);
		System.out.println("Is a Short: " + ((short) 2));
		System.out.println("Is a Boolean: " + true);
    }
}
