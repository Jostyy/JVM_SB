package main;

public class double_aritmetica
{
    public static void main(final String[] array) {
        final double n = 98.24;
        final double n2 = 134.87;
        System.out.println(n + n2);
        System.out.println(n - n2);
        System.out.println(n * n2);
        System.out.println(n / n2);
        System.out.println(-n);
        System.out.println(n2 % n);
    }
}